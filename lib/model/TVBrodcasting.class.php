	public static function getChannels($country){
		$tvChannels = array();
		if (array_key_exists($country, self::$COUNTRIES_TO_TV_BRODCASTING)) $tvChannels = self::$COUNTRIES_TO_TV_BRODCASTING[$country];
		
		return $tvChannels;
	}
	
	public static function getEventDesc($country, $isHtml = false){
		$tvChannels = self::getChannels($country);
		
		$descStr = '';
		
		if (count($tvChannels)){
			if ($isHtml) $descStr .= "<br/>TV Broadcasting in $country:<br/>";
			else $descStr .= "\nTV Broadcasting in $country:\n";
		}
		
		foreach ($tvChannels as $tvChannel){
			if ($isHtml) $descStr .= '<a href="' . $tvChannel['url'] . '"' . ($tvChannel['desc'] ? ' title="' . $tvChannel['desc'] . '"' : '') . '>' . $tvChannel['name'] . '</a><br/>';
			else $descStr .= $tvChannel['name'] . " : " . $tvChannel['url'] . "\n";
		}

		
		return $descStr;
	}
